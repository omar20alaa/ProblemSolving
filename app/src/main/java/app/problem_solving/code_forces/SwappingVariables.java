package app.problem_solving.code_forces;

class SwappingVariables {

    public static void main(String[] args) {

        System.out.println("Welcome To Swapping Variables");

        int x = 10 ;
        int y = 20;
        int temp = x;

        x = y;
        y = temp;

        System.out.println("x is --> " + x);
        System.out.println("y is --> " + y);

    }


}
